// Regenerates lib/pomodoro/tracks.ts from the audio files in public/audio/focus/.
// Run after adding or removing tracks:  node scripts/gen-focus-tracks.mjs
import { readdirSync, writeFileSync } from "node:fs";

const DIR     = "public/audio/focus";
const OUT     = "lib/pomodoro/tracks.ts";
const AUDIO_RE = /\.(mp3|ogg|m4a|wav)$/i;

const files = readdirSync(DIR).filter(f => AUDIO_RE.test(f)).sort();
// Encode the filename (spaces, parens) but keep the path separators readable.
const srcs = files.map(f => `/audio/focus/${encodeURIComponent(f)}`);

const body = `// AUTO-GENERATED by scripts/gen-focus-tracks.mjs — do not edit by hand.
// Re-run \`node scripts/gen-focus-tracks.mjs\` after adding/removing files in
// public/audio/focus/. Focus music plays a random track from this list; an empty
// list hides the music control entirely.

export const FOCUS_TRACKS: string[] = [
${srcs.map(s => `  ${JSON.stringify(s)},`).join("\n")}
];
`;

writeFileSync(OUT, body);
console.log(`Wrote ${srcs.length} track(s) to ${OUT}`);
